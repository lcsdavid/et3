package com.logique;

public class Entrée {
    private boolean val;

    public Entrée() {
        val = false;
    }

    public Entrée(boolean val) {
        this.val = val;
    }

    public void setVal(boolean val) {
        this.val = val;
    }

    public boolean getVal() {
        return val;
    }
}
